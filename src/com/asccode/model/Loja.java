package com.asccode.model;

public class Loja {
	
	private Integer id;
	private String nome;
	private String key;
	
	public Integer getId(){
		return id;
	}
	public void setId(Integer id){
		this.id = id;
	}	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}

}
